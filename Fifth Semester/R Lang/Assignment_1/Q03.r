# Q03. Write a R program to find the perimeter and area of rectangle

length <- 8
width <- 5

# Calculate the area of the rectangle
area <- length * width

# Calculate the perimeter of the rectangle
perimeter <- 2 * (length + width)

cat("Length of the rectangle: ", length, "\n")
cat("Width of the rectangle: ", width, "\n")
cat("Area of the rectangle: ", area, "\n")
cat("Perimeter of the rectangle: ", perimeter, "\n")
